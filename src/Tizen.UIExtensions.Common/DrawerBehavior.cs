namespace Tizen.UIExtensions.Common
{
    /// <summary>
    /// Enumerates values that describe behaviors of the drawer in NavigationDrawer.
    /// </summary>
    public enum DrawerBehavior
    {
        /// <summary>
        /// Disable the drawer.
        /// </summary>
        Disabled,

        /// <summary>
        /// Show the drawer when it is opened.
        /// </summary>
        Drawer,

        /// <summary>
        /// Hold the drawer open.
        /// </summary>
        Locked
    }
}
